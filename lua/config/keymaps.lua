local set = vim.keymap.set

-- Misc Leader Actions
set("n", "<leader>/", function ()
    vim.cmd.Pick("grep_live")
end)
set("n", "<leader>ht", function()
	vim.cmd.Pick("help")
end)
set({"v", "n"}, "<leader>y", '"+y')
set("n", "<leader>ut", vim.cmd.UndotreeToggle)
-- Buffer Actions
set("n", "<leader>bb", function()
	vim.cmd.Pick("buffers")
end)
set("n", "<leader>bd", vim.cmd.CloseBuffer)
-- Code / Compile Actions
set("n", "<leader>ca", vim.lsp.buf.code_action)
set("n", "<leader>cr", vim.lsp.buf.rename)
set("n", "<leader>cc", function()
	vim.cmd.ReadCmd()
end)
set("n", "<leader>cf", vim.lsp.buf.format)
set("n", "<leader>cl", function()
	vim.cmd.checkhealth("vim.lsp")
end)
-- File Actions
set("n", "<leader>ff", function ()
    vim.cmd.Pick("files")
end)
-- Git Actions
set("n", "<leader>gC", function()
	local branch = vim.fn.input("Branch name: ")
	if branch ~= "" then
		vim.cmd.Git("checkout -b " .. branch)
		vim.cmd.Git("push -u origin " .. branch)
	end
end)
set("n", "<leader>gg", vim.cmd.Git)
set("n", "<leader>gD", vim.cmd.Gdiffsplit)
set("n", "<leader>gb", function ()
    vim.cmd.GitPick("branches")
end)
set("n", "<leader>gA", function()
	vim.cmd.Git("add --all")
end)
set("n", "<leader>ga", function ()
    vim.cmd.GitPick("add")
end)
set("n", "<leader>gc", function()
	vim.cmd.Git("commit")
end)
set("n", "<leader>gp", function()
	vim.cmd.Git("push")
end)
set("n", "<leader>gP", function()
	vim.cmd.Git("pull --rebase")
end)
set("n", "<leader>gB", function()
	vim.cmd.Git("blame")
end)
set("n", "<leader>gM", function()
	vim.cmd.Git("mergetool")
end)
set("n", "<leader>gd", function()
	vim.cmd.Git("diff")
end)
set("n", "<leader>gl", function()
	vim.cmd.Git("log")
end)
set("n", "<leader>gu", function ()
    vim.cmd.GitPick("unstage")
end)
set("n", "<leader>gU", function()
	vim.cmd.Git("restore --staged :/")
end)
set("n", "<leader>gr", function ()
    vim.cmd.GitPick("restore")
end)
set("n", "<leader>gR", function ()
	local confirm = vim.fn.input("Do you want to restore ALL files(yes/no): ")
	if confirm == "yes" then
		vim.cmd.Git("restore :/")
	end
end)
set("n", "<leader>ge", function ()
    vim.cmd.GitPick("rebase")
end)
set("n", "<leader>gF", function()
	vim.cmd.Git("fetch")
end)
set("n", "<leader>gj", function()
	vim.cmd("diffget //2<CR>")
end)
set("n", "<leader>gf", function()
	vim.cmd("diffget //3<CR>")
end)
set("n", "<leader>go", vim.cmd.PrintConflicts)
set("n", "<leader>gss", function ()
    vim.cmd.GitPick("stash")
end)
set("n", "<leader>gsS", function ()
    vim.cmd.Git("stash")
end)
set("n", "<leader>gsp", function()
    vim.cmd.GitPick("stash_pop")
end)
set("n", "<leader>gsc", function()
	vim.cmd.Git("stash clear")
end)
set("n", "<leader>gmu", function ()
    vim.cmd.GitPick("update_submodules")
end)
-- Treesitter Actions
set("n", "<leader>tt", vim.cmd.InspectTree)
set("n", "<leader>te", vim.cmd.EditQuery)
set("n", "<leader>tv", function ()
    vim.cmd.TreesitterPick("buffer_var_query")
end)
set("n" ,"<leader>tf", function ()
    vim.cmd.TreesitterPick("buffer_func_query")
end)
set("n", "<leader>tF", function ()
    vim.cmd.TreesitterPick("buffer_field_query")
end)
set("n", "<leader>ti", vim.cmd.TSInstalled)
-- Project Actions
set("n", "<leader>pf", vim.cmd.GitFilesPick)
set("n", "<leader>pd", function ()
    vim.cmd.MiscPick("dirs")
end)
set("n", "<leader>qq", vim.cmd.Ex)
-- Window Actions
set("n", "<leader>ws", vim.cmd.split)
set("n", "<leader>wv", vim.cmd.vsplit)
set("n", "<leader>wq", vim.cmd.close)
set("n", "<leader>tl", function ()
    vim.cmd.MiscCliPick("tldr")
end)
-- Misc Actions
set("n", "gd", function()
    vim.cmd.MiscPick("definition")
end)
set("n", "gr", function()
    vim.cmd.MiscPick("reference")
end)
