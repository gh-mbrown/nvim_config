local set = vim.keymap.set

-- Misc Leader Actions
set({ "v", "n" }, "<leader>y", '"+y')
-- Plugin Actions
set("n", "<leader>pu", vim.cmd.UpdatePlugins)
set("n", "<leader>pc", vim.cmd.CleanPlugins)
set("n", "<leader>bd", vim.cmd.CloseBuffer)
-- Code / Compile Actions
set("n", "<leader>ca", vim.lsp.buf.code_action)
set("n", "<leader>cr", vim.lsp.buf.rename)
set("n", "<leader>cc", vim.cmd.ReadCmd)
set("n", "<leader>cf", vim.lsp.buf.format)
set("n", "<leader>cl", function()
    vim.cmd.checkhealth("vim.lsp")
end)
set("n", "<leader>qq", vim.cmd.Ex)
-- Window Actions
set("n", "<leader>ws", vim.cmd.split)
set("n", "<leader>wv", vim.cmd.vsplit)
set("n", "<leader>wq", vim.cmd.close)
set("n", "gd", vim.lsp.buf.definition)
set("n", "gr", vim.lsp.buf.references)
set("n", "gi", vim.lsp.buf.implementation)
set("v", "<", "<gv")
set("v", ">", ">gv")
set("n", "<", "<<")
set("n", ">", ">>")
set("v", "p", "P")
set("n", "<C-n>", function()
    vim.cmd("nohlsearch")
end)
set("v", "J", ":m '>+1<CR>gv=gv")
set("v", "K", ":m '<-2<CR>gv=gv")
set("n", "K", vim.lsp.buf.hover)
set("n", "J", vim.diagnostic.open_float)
